/**
 * The `BedType` enum represents the type of bed in a hotel room.
 *
 * @constant SINGLE a single bed
 * @constant QUEEN a queen-sized bed
 * @constant KING a king-sized bed
 * @constant DOUBLE two double beds
 * @author Albertus Timothy
 */
public enum BedType {
    SINGLE,
    QUEEN,
    KING,
    DOUBLE
}
